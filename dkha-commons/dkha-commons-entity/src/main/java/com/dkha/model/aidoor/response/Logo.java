package com.dkha.model.aidoor.response;

public class Logo {

    private String mainLogo;

    private String mainLogoId;

    private String secondLogo;

    private String secondLogoId;

    public String getMainLogo() {
        return mainLogo;
    }

    public void setMainLogo(String mainLogo) {
        this.mainLogo = mainLogo;
    }

    public String getMainLogoId() {
        return mainLogoId;
    }

    public void setMainLogoId(String mainLogoId) {
        this.mainLogoId = mainLogoId;
    }

    public String getSecondLogo() {
        return secondLogo;
    }

    public void setSecondLogo(String secondLogo) {
        this.secondLogo = secondLogo;
    }

    public String getSecondLogoId() {
        return secondLogoId;
    }

    public void setSecondLogoId(String secondLogoId) {
        this.secondLogoId = secondLogoId;
    }
}
